#include <iostream>

using namespace std;

int main() {
	cout << "int는 " << sizeof(int) << "byte이다." << endl;
	cout << "char는 " << sizeof(char) << "byte이다." << endl;
	cout << "short는 " << sizeof(short) << "byte이다." << endl;
	cout << "float는 " << sizeof(float) << "byte이다." << endl;
	cout << "long은 " << sizeof(long) << "byte이다." << endl;
}